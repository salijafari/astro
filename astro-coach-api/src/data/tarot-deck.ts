export const MINOR_ARCANA = TAROT_DECK.filter((c) => c.arcana === "minor");

/** Lookup by spread draw id (`major-00`, `wands-01`, …). */
export function getCardById(deckId: string): TarotCard | undefined {
  return TAROT_DECK.find((c) => c.deckId === deckId);
}

/** Matches legacy `TarotCardContent.cardId` (0–77). */
export function getCardByNumericId(id: number): TarotCard | undefined {
  return TAROT_DECK.find((c) => c.id === id);
}
